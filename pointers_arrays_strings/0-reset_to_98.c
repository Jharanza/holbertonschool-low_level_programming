#include "main.h"

/**
 * reset_to_98 - Update a variable with a pointer
 *
 * @*n: Pointer with int value
 *
 * @n: Variable with the pointer.
 *
 * function takes a pointer to an int as parameter and updates the
 * value it points to to 98
 *
 * Return: void
 */

void reset_to_98(int *n)
{
	*n = 98;
}
